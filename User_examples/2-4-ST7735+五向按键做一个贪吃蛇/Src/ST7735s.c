void HWSPI_Init()
{
    P3_MOD_OC &= ~((1 << 7) | (1 << 5)); //SCK1, MOSI1 推挽输出
    P3_DIR_PU |= ((1 << 7) | (1 << 5));
    SPI1_CK_SE = 0x06;           //设置SPI1时钟分频系数： SPI速度 = 主频/SPI1_CK_SE
    SPI1_CTRL &= ~(bS1_CLR_ALL); // 重要！！！！不操作会导致无法工作：该位为1 清空SPI1中断标志和FIFO，需要软件清零，
    SPI1_CTRL |= bS1_SCK_OE;     //SPI1的SCK1 输出使能控制位，该位为1允许SCK1输出，如果bS1_2_WIRE=0，那么将同时允许MOSI1 输出使能；该位为0 禁止输出
}

//向SPI总线传输一个8位数据
void spi_write_byte(uchar Data)
{
    SPI1_DATA = Data;
    while (!(SPI1_STAT & 0x08))
        ;
}
/*****************************************************************************
 * @name       :void LCD_WR_REG(u8 data)
 * @date       :2018-08-09 
 * @function   :Write an 8-bit command to the LCD screen  向液晶屏写一个8位指令
 * @parameters :data:Command value to be written
 * @retvalue   :None
******************************************************************************/
//向液晶屏写一个8位指令
void LCD_WR_REG(u8 Reg)
{
    LCD_RS = 0;
    LCD_CS = 0;
    spi_write_byte(Reg);
    LCD_CS = 1;
}
/*****************************************************************************
 * @name       :void LCD_WR_DATA(u8 data)
 * @date       :2018-08-09 
 * @function   :Write an 8-bit data to the LCD screen
 * @parameters :data:data value to be written
 * @retvalue   :None
******************************************************************************/
//向液晶屏写一个8位数据
void LCD_WR_DATA(u8 Data)
{
    LCD_RS = 1;
    LCD_CS = 0;
    spi_write_byte(Data);
    LCD_CS = 1;
}

//向液晶屏写一个16位数据
/*****************************************************************************
 * @name       :void LCD_WR_DATA_16Bit(u16 Data)
 * @date       :2018-08-09 
 * @function   :Write an 16-bit command to the LCD screen
 * @parameters :Data:Data to be written
 * @retvalue   :None
******************************************************************************/
void LCD_WR_DATA_16Bit(u16 Data)
{
    LCD_RS = 1;
    LCD_CS = 0;
    spi_write_byte(Data >> 8);
    spi_write_byte(Data);
    LCD_CS = 1;
}

/*****************************************************************************
 * @name       :void LCD_WriteReg(u8 LCD_Reg, u16 LCD_RegValue)
 * @date       :2018-08-09 
 * @function   :Write data into registers
 * @parameters :LCD_Reg:Register address
                LCD_RegValue:Data to be written
 * @retvalue   :None
******************************************************************************/
void LCD_WriteReg(u8 LCD_Reg, u8 LCD_RegValue)
{
    LCD_WR_REG(LCD_Reg);
    LCD_WR_DATA(LCD_RegValue);
}

