/*************************************************
函数名：LCD_Set_Region
功能：设置lcd显示区域，在此区域写点数据自动换行
入口参数：xy起点和终点
返回值：无
*************************************************/
void Lcd_SetRegion(unsigned int x_start, unsigned int y_start, unsigned int x_end, unsigned int y_end)
{
#ifdef USE_LANDSCAPE //使用横屏模式
    Lcd_WriteIndex(0x2a);
    Lcd_WriteData(0x02);
    Lcd_WriteData(x_start + 2);
    Lcd_WriteData(0x02);
    Lcd_WriteData(x_end + 2);

    Lcd_WriteIndex(0x2b);
    Lcd_WriteData(0x01);
    Lcd_WriteData(y_start + 1);
    Lcd_WriteData(0x01);
    Lcd_WriteData(y_end + 1);

#else //竖屏模式
    Lcd_WriteIndex(0x2a);
    Lcd_WriteData(0x02);
    Lcd_WriteData(x_start + 2);
    Lcd_WriteData(0x02);
    Lcd_WriteData(x_end + 2);

    Lcd_WriteIndex(0x2b);
    Lcd_WriteData(0x01);
    Lcd_WriteData(y_start + 1);
    Lcd_WriteData(0x01);
    Lcd_WriteData(y_end + 1);
#endif
    Lcd_WriteIndex(0x2c);
}

void PutPixel(uint x_start, uint y_start, uint color)
{
    Lcd_SetRegion(x_start, y_start, x_start + 1, y_start + 1);
    LCD_WriteData_16Bit(color);
}

void dsp_single_colour(unsigned int color)
{
    uchar i, j;
    Lcd_SetRegion(0, 0, 128 - 1, 160 - 1);
    for (i = 0; i < 160; i++)
        for (j = 0; j < 128; j++)
            LCD_WriteData_16Bit(color);
}

void dsp_single_colour_by_line(int color, int line)
{
    uchar i, j;
    Lcd_SetRegion(0, 0, 128 - 1, 10 + line * 16 - 1);
    for (i = 0; i < 10 + line * 16; i++)
        for (j = 0; j < 128; j++)
            LCD_WriteData_16Bit(color);
}

